// Declare a setup function
function setup(){
  //creating a canvas 500px by 500px
  createCanvas( 500, 500);
  //coloring the background 'orange'
  background( 'orange');
}

//Declare a draw function
function draw(){
  //Tell p5 to print a text labeled "Hello World!"
  //This will be positioned at (x:50, y:100)
  text( "Hello World!", 50, 100);
}
